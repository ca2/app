int main()
{

   auto pmemory1 = create_archive_memory();
   auto pmemory2 = create_archive_memory();

   ::resource_folder_pool pool;

   pool.reconcile(2);

   ::std::atomic_bool bSucceeded = true;
   ::std::thread threada[8];

   for (::collection::index i = 0; i < 8; i++)
   {

      auto pmemory = (i % 2 == 0) ? pmemory1 : pmemory2;
      auto pszPath = (i % 3 == 0) ? "popup_lyricimpact.menu" : "system/user/authenticate.xhtml";
      auto pszExpected = (i % 3 == 0) ? g_pszMenu : g_pszAuthentication;

      threada[i] = ::std::thread(
         repeat_reads,
         &pool,
         pmemory,
         pszPath,
         pszExpected,
         ::std::ref(bSucceeded));

   }

   for (auto & thread : threada)
   {

      thread.join();

   }

   return bSucceeded ? 0 : 1;

}
