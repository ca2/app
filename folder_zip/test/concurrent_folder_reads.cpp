int main()
{

   ::pointer < read_only_memory > pmemory(allocate_t{}, g_zip, sizeof(g_zip));
   ::pointer < memory_file > pmemoryfile(allocate_t{}, pmemory.m_p);
   ::file_pointer parchive = pmemoryfile;
   ::pointer < ::folder_zip::folder > pfolder(allocate_t{});

   pfolder->open_for_reading(parchive);

   auto pfile1 = pfolder->get_file("one.txt");
   auto pfile2 = pfolder->get_file("two.txt");

   if (!pfile1 || !pfile2)
   {

      return 1;

   }

   if (!reads_as(pfile1, "111111"))
   {

      return 2;

   }
   if (!reads_as(pfile2, "22222222"))
   {

      return 3;

   }
   pfile1->close();
   pfile2->close();

   ::folder_zip::global_session_pool().reconcile(2);

   ::std::atomic_bool bSucceeded = true;

   ::std::thread thread1(repeat_reads, pfolder.m_p, "one.txt", "111111", ::std::ref(bSucceeded));
   ::std::thread thread2(repeat_reads, pfolder.m_p, "two.txt", "22222222", ::std::ref(bSucceeded));

   thread1.join();
   thread2.join();

   if (!bSucceeded)
   {

      return 4;

   }

   ::pointer < read_only_memory > pmemory2(allocate_t{}, g_zip, sizeof(g_zip));
   ::pointer < memory_file > pmemoryfile2(allocate_t{}, pmemory2.m_p);
   ::file_pointer parchive2 = pmemoryfile2;
   ::pointer < ::folder_zip::folder > pfolder2(allocate_t{});

   pfolder2->open_for_reading(parchive2);

   ::std::thread thread3(repeat_reads, pfolder.m_p, "one.txt", "111111", ::std::ref(bSucceeded));
   ::std::thread thread4(repeat_reads, pfolder2.m_p, "two.txt", "22222222", ::std::ref(bSucceeded));

   thread3.join();
   thread4.join();

   if (!bSucceeded)
   {

      return 5;

   }

   return 0;

}
