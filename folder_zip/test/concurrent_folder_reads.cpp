   bool reads_as(::file::file * pfile, const_char_pointer pszExpected)
   {

      char sz[32]{};

      auto iRead = pfile->read(sz, sizeof(sz));

      return ::string(sz, iRead) == pszExpected;

   }


   void repeat_reads(
      ::folder_zip::folder * pfolder,
      const_char_pointer pszPath,
      const_char_pointer pszExpected,
      ::std::atomic_bool & bSucceeded)
   {

      for (::collection::index i = 0; i < 100 && bSucceeded; i++)
      {

         auto pfile = pfolder->get_file(pszPath);

         if (!pfile || !reads_as(pfile, pszExpected))
         {

            bSucceeded = false;

         }

      }

   }


