   ::pointer < memory_base > create_archive_memory()
   {

      ::pointer < read_only_memory > pmemory(
         allocate_t{}, g_zip, (::memsize) sizeof(g_zip));

      return pmemory;

   }


   resource_folder_lease acquire_folder(
      ::resource_folder_pool & pool,
      const ::pointer < memory_base > & pmemory)
   {

      ::function < ::pointer < ::folder >() > functionCreateFolder = [pmemory]
         {

            ::pointer < ::folder_zip::folder > pzipfolder(allocate_t{});
            ::pointer < memory_file > pfile(allocate_t{}, pmemory.m_p);

            pzipfolder->open_for_reading(pfile);

            return ::pointer < ::folder >(pzipfolder);

         };

      return pool.acquire(pmemory, functionCreateFolder);

   }


   bool open_located_file(::folder_zip::folder * pfolder)
   {

      if (unzip_OpenCurrentFile(pfolder->m_unzip_file) != UNZ_OK)
      {

         return false;

      }

      return unzip_GetCurrentFileInfo(
         pfolder->m_unzip_file,
         &pfolder->m_unzip_fileinfo,
         nullptr,
         0,
         nullptr,
         0,
         nullptr,
         0) == UNZ_OK;

   }


   bool read_matches(
      ::resource_folder_pool & pool,
      const ::pointer < memory_base > & pmemory,
      const_char_pointer pszPath,
      const_char_pointer pszExpected)
   {

      auto folderlease = acquire_folder(pool, pmemory);

      if (!folderlease)
      {

         return false;

      }

      if (!folderlease->locate(
         [pszPath](const_char_pointer pszItem) { return ::strcmp(pszItem, pszPath) == 0; }))
      {

         return false;

      }

      auto pzipfolder = dynamic_cast < ::folder_zip::folder * >(folderlease.get());

      if (!pzipfolder || !open_located_file(pzipfolder))
      {

         return false;

      }

      auto pfile = folderlease->get_file();

      if (!pfile)
      {

         return false;

      }

      char sz[512]{};
      auto sRead = pfile->read(sz, sizeof(sz));
      auto sExpected = ::strlen(pszExpected);

      return sRead == sExpected && ::memcmp(sz, pszExpected, sExpected) == 0;

   }


   void repeat_reads(
      ::resource_folder_pool * ppool,
      ::pointer < memory_base > pmemory,
      const_char_pointer pszPath,
      const_char_pointer pszExpected,
      ::std::atomic_bool & bSucceeded)
   {

      for (::collection::index i = 0; i < 100 && bSucceeded; i++)
      {

         if (!read_matches(*ppool, pmemory, pszPath, pszExpected))
         {

            bSucceeded = false;

         }

      }

   }


